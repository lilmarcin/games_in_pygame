def generate_ball(i):
    radius = 5
    x = 45 + 2*radius * (i % 60)
    y = 50 + 2*radius * (i // 80)
    #print(x)
    #print(y)
    #print(f"----------------{i}---------------------------")
    color = (255, 128, 0) #colors[i % len(colors)]
    mass = 0.4
    return Ball(space, x, y, radius, color, mass)


# Main loop
def main():
    simulation_started = False
    obstacles = initialize_map()
    balls = [generate_ball(i) for i in range(400)]  # 400 balls on start
    while True:
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                pygame.quit()
                sys.exit()
            elif event.type == pygame.KEYDOWN and event.key == pygame.K_SPACE:
                simulation_started = True

            elif event.type == pygame.MOUSEBUTTONDOWN and event.button == 1:
                        mouse_x, mouse_y = event.pos
                        color = (255, 128, 0) # random.choice(colors)
                        radius = 5
                        mass = 0.4
                        new_ball = Ball(space, mouse_x, mouse_y, radius, color, mass)
                        balls.append(new_ball)
    
        if simulation_started:
            space.step(1/fps)
            space.debug_draw(draw_options)


        screen.fill(black) 

        # Draw obstacles and balls
        for ball in balls:
            ball.draw(screen)
        for obstacle in obstacles:
            obstacle.draw(screen)

        pygame.display.flip()

        clock.tick(fps)

if __name__ == "__main__":
    main()